    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        cod = idMObra();
        Mes = cbMes.getSelectedItem().toString();
        Placa = cbVehiculos.getSelectedItem().toString();
        Mantenimeinto = txtMantenimiento.getText();
        Repuesto = txtRepuesto.getText();
        codVe = CodVehiculo(); 

        PreparedStatement pst = null;
        //           String sql = "INSERT INTO Empleado VALUES(?,?,?,?,?,?)";

        if(Mantenimeinto.equals("") || Repuesto.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar Descripcion \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            cbVehiculos.requestFocus();

        }else {

            try{

                pst=cn.prepareStatement("INSERT INTO Mano_Obra(cod,mantenimiento,repuesto,vehiculo,matricula,fecha_m) VALUES(?,?,?,?,?,?)");
                pst.setInt(1,cod);
                pst.setString(2,Mantenimeinto);
                pst.setString(3,Repuesto);
                pst.setInt(4,codVe);
                pst.setString(5,Placa);
                pst.setString(6,Mes);
               
                //                    JOptionPane.showMessageDialog(null, "Empleado"+ id +"Registrado" );

                int a=pst.executeUpdate();
                if(a>0){
                    JOptionPane.showMessageDialog(null,"Registro exitoso");
                }else{
                    JOptionPane.showMessageDialog(null,"Error al agregar");
                }
            }catch(HeadlessException | SQLException ex){
                Logger.getLogger(EViaje.class.getName()).log(Level.SEVERE, null, ex);
            }

            finally {
                if (cn != null) {
                    try {
                        cn.close();
                        //pst.close();
                    } catch ( SQLException e ) {
                        System.out.println( e.getMessage());
                    }
                }
            }

            javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }

        this.txtMantenimiento.setText("");
        //        this.cbEmpleado.setSelectedItem(" ");
        this.txtRepuesto.setText("");
        //        this.date_salida.setDate(salida);
        //        this.date_llegada.setDate(llegada);
        this.cbVehiculos.setSelectedItem("");
        this.cbMes.setSelectedItem("");
    }//GEN-LAST:event_btnguardarActionPerformed

