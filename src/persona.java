    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        
        id=idPersona.getText();
        persona = cbPersona.getSelectedItem().toString();
        if (id.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"ingrese cedula o ruc \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                idPersona.requestFocus();
        }else{
            try{
                PreparedStatement pst=cn.prepareStatement("INSERT INTO Persona(id) VALUES(?)");
                pst.setString(1,idPersona.getText());

                int a=pst.executeUpdate();
                if(a>0 && persona.equalsIgnoreCase("EMPLEADO") ){
                    JOptionPane.showMessageDialog(null,"Registro exitoso");
                    
                    REmpleado re = new REmpleado();
                    re.setVisible(true);
                    this.setVisible(false);
                }else if(a>0 && persona.equalsIgnoreCase("CLIENTE")){
                    JOptionPane.showMessageDialog(null,"Registro exitoso");
                    
                    RCliente rc = new RCliente();
                    rc.setVisible(true);
                    this.setVisible(false);
                }else if(a>0 && persona.equalsIgnoreCase("PROVEEDOR")){
                    JOptionPane.showMessageDialog(null,"Registro exitoso");
                    
                    RProveedor rp = new RProveedor();
                    rp.setVisible(true);
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(null,"Error al agregar");
                }
            }catch(Exception e){
            }  
        }// TODO
    }//GEN-LAST:event_btnAceptarActionPerformed

