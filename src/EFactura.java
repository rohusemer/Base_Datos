    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        fecha = cbDate.getDate();
        fecha_m = convertUtilToSql(fecha);
        tipo = cbTipo.getSelectedItem().toString();
        idTipo = Integer.parseInt(cbId.getSelectedItem().toString());
        des = txtDescripcion.getText();
        precio = txtValor.getText();
        valor = Integer.parseInt(precio);
        articulo = txtArticulo.getText();
//        idc = id_autoincremente();
        PreparedStatement pst = null;
        //           String sql = "INSERT INTO Empleado VALUES(?,?,?,?,?,?)";

        if(tipo.equals("VIAJE")){
            if(des.equals("")|| precio.equals("")|| articulo.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            cbId.requestFocus();

            }else {
                try{
                        pst=cn.prepareStatement("UPDATE factura_viaje SET num_defactura ='"+idTipo+
                        "', fecha_registro ='"+fecha_m+"',descripcion='"+des+"',valor='"+valor+
                        "',articulo ='"+articulo+"'  WHERE num_defactura='"+idTipo+"'");
                
                        idTipo = Integer.parseInt(cbId.getSelectedItem().toString());
                        pst.executeUpdate();
            } catch(HeadlessException | SQLException ex){
                Logger.getLogger(EViaje.class.getName()).log(Level.SEVERE, null, ex);
            }

            finally {
                if (cn != null) {
                    try {
                        cn.close();
                        pst.close();
                    } catch ( SQLException e ) {
                        System.out.println( e.getMessage());
                    }
                }
            }

                javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else if(tipo.equals("PROVEEDOR")) {
            if(des.equals("")|| precio.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            cbId.requestFocus();

            }else {
                try{
                        pst=cn.prepareStatement("UPDATE factura_proveedor SET num_defactura ='"+idTipo+
                        "', fecha_registro ='"+fecha_m+"',descripcion='"+des+
                        "',valor='"+valor+"' WHERE num_defactura='"+idTipo+"'");
                
                        idTipo = Integer.parseInt(cbId.getSelectedItem().toString());
                        pst.executeUpdate();
            } catch(HeadlessException | SQLException ex){
                Logger.getLogger(EViaje.class.getName()).log(Level.SEVERE, null, ex);
            }

            finally {
                if (cn != null) {
                    try {
                        cn.close();
                        pst.close();
                    } catch ( SQLException e ) {
                        System.out.println( e.getMessage());
                    }
                }
            }

                javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        }
        this.cbTipo.setSelectedItem("");
        this.cbId.setSelectedItem("");
        this.txtDescripcion.setText("");
        this.txtValor.setText("");
         this.txtArticulo.setText("");
        
    }//GEN-LAST:event_btnguardarActionPerformed

