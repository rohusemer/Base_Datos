    void mostrardatos(String valor){
        DefaultTableModel modelo= new DefaultTableModel();
        modelo.addColumn("Codigo de Pago");
        modelo.addColumn("Identidicacion");
        modelo.addColumn("Nombres");
        modelo.addColumn("Tipo");    
        
        tbPagos.setModel(modelo);
        String sql="";
        if (valor.equals("")){
            //sql = "SELECT p.codigo_p, p.empleado, p.proveedor, e.primer_nombre, pr.nombre FROM Pagos p, Empleado e,Proveedor pr WHERE e.id = p.empleado OR pr.id = p.proveedor GROUP BY p.codigo_p";
            sql ="SELECT * FROM(SELECT DISTINCT p.codigo_p, p.empleado, UPPER(CONCAT(e.primer_nombre,\" \",e.primer_apeelido)) as Nombre, 'Empleados'"+
            " FROM Pagos p INNER JOIN Empleado e ON e.id = p.empleado"+
            " UNION SELECT DISTINCT p.codigo_p, p.proveedor,UPPER(CONCAT(pr.nombre,\" \",pr.apellido)) as Nombre, 'Proveedor'"+
            " FROM Pagos p INNER JOIN Proveedor pr ON pr.id = p.proveedor) as p"+
            " GROUP BY p.p.codigo_p ORDER BY p.p.codigo_p";        
        } else {
            sql = "SELECT * FROM(SELECT DISTINCT p.codigo_p, p.empleado, UPPER(CONCAT(e.primer_nombre,\" \",e.primer_apeelido)) as Nombre, 'Empleados'"+
            " FROM Pagos p INNER JOIN Empleado e ON e.id = p.empleado"+
            " UNION SELECT DISTINCT p.codigo_p, p.proveedor,UPPER(CONCAT(pr.nombre,\" \",pr.apellido)) as Nombre, 'Proveedor'"+
            " FROM Pagos p INNER JOIN Proveedor pr ON pr.id = p.proveedor) as p  HAVING p.Nombre like '%"+valor+"%'";
        }
    
        String [] datos = new String[4];
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                modelo.addRow(datos);
            }
            tbPagos.setModel(modelo);
        } catch (SQLException ex){
           Logger.getLogger(REmpleado.class.getName()).log(Level.SEVERE, null, ex); 

        }
    }

