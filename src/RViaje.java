    public int idViaje(){
        int id=1;
        String sql = "SELECT MAX(codigo) FROM Viaje";
        try {
            
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                id=rs.getInt(1) +1 ;
            }
        } catch (SQLException ex) {
            Logger.getLogger(RViaje.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
