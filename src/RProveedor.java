    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       
        cid = txtiD.getText();
        Alias = txtFAlias.getText();
        Nombre = txtFNombre.getText();
        Apellido = txtFApellido.getText();
        
        PreparedStatement pst = null;
        //           String sql = "INSERT INTO Empleado VALUES(?,?,?,?,?,?)";

        if(cid.equals("")||Alias.equals("")||Nombre.equals("")||Apellido.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            txtiD.requestFocus();

        }else {

            try{

                pst=cn.prepareStatement("INSERT INTO Proveedor(id,nombre,apellido,alias) VALUES(?,?,?,?)");
                pst.setString(1,cid);//tfidPersona.getText());
            pst.setString(2,Nombre);
            pst.setString(3,Apellido);
            pst.setString(4,Alias);
           

            //                    JOptionPane.showMessageDialog(null, "Empleado"+ id +"Registrado" );

            int a=pst.executeUpdate();
            if(a>0){
                
                JOptionPane.showMessageDialog(null,"Registro exitoso");
            }else{
                JOptionPane.showMessageDialog(null,"Error al agregar");
            }
        }catch(HeadlessException | SQLException ex){
            Logger.getLogger(REmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }

       
            finally {
                if (cn != null) {
                    try {
                        cn.close();
                        pst.close();
                    } catch ( SQLException e ) {
                        System.out.println( e.getMessage());
                    }
                }
            }

            javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        this.txtiD.setText("");
        this.txtFNombre.setText("");
      
        this.txtFApellido.setText("");
        this.txtFAlias.setText("");

    }//GEN-LAST:event_btnguardarActionPerformed

