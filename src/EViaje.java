     private String CodEmpleado(){
        String nombre= cbEmpleado.getSelectedItem().toString();
        String id = "";
        String sql = "SELECT id FROM Empleado WHERE concat(primer_nombre, \" \" , primer_apeelido) = '"+nombre+"'";
        try {
            
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                id=rs.getString(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(REmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;  
    } 
