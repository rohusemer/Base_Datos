    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        fecha = cbDate.getDate();
        fecha_m = convertUtilToSql(fecha);
        Proveedor = cbProveedor.getSelectedItem().toString();
        des = txtDescripcion.getText();
        codObra = Integer.parseInt(cbManoObra.getSelectedItem().toString());
        precio = txtValor.getText();
        valor = Integer.parseInt(precio);
        codFactura = id();
        codProveedor = CodEmpleado();
        
        PreparedStatement pst = null;
        //           String sql = "INSERT INTO Empleado VALUES(?,?,?,?,?,?)";

        if(Proveedor.equals("")|| des.equals("")|| precio.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            cbManoObra.requestFocus();

        }else {

            try{

                pst=cn.prepareStatement("INSERT INTO factura_proveedor(num_defactura,fecha_registro,descripcion,valor,mano_obra,proveedor) VALUES(?,?,?,?,?,?)");
                pst.setInt(1,codFactura);//tfidPersona.getText());
            pst.setDate(2,fecha_m);
            pst.setString(3,des);
            pst.setFloat(4,valor);
            pst.setInt(5,codObra);
            pst.setString(6,codProveedor);
            

            //                    JOptionPane.showMessageDialog(null, "Empleado"+ id +"Registrado" );

            int a=pst.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro exitoso");
            }else{
                JOptionPane.showMessageDialog(null,"Error al agregar");
            }
        }catch(HeadlessException | SQLException ex){
            Logger.getLogger(RVehiculo.class.getName()).log(Level.SEVERE, null, ex);
        }

        finally {
            if (cn != null) {
                try {
                    cn.close();
                    pst.close();
                } catch ( SQLException e ) {
                    System.out.println( e.getMessage());
                }
            }
        }

        javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
       

        this.cbProveedor.setSelectedItem("");
        this.cbManoObra.setSelectedItem("");
        this.txtDescripcion.setText("");
        this.txtValor.setText("");
        
    }//GEN-LAST:event_btnguardarActionPerformed

