    public int id(){
        int id=1;
        String sql = "SELECT MAX(num_defactura) FROM factura_viaje";
        try {
            
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                id=rs.getInt(1) +1 ;
            }
        } catch (SQLException ex) {
            Logger.getLogger(RFViaje.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
