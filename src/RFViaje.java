    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        fecha = cbDate.getDate();
        fecha_m = convertUtilToSql(fecha);
        Cliente = cbCliente.getSelectedItem().toString();
        des = txtDescripcion.getText();
        codViaje = Integer.parseInt(cbViaje.getSelectedItem().toString());
        precio = txtValor.getText();
        valor = Integer.parseInt(precio);
        codFactura = id();
        codCliente = CodCliente();
        articulo = txtArticulo.getText();

        PreparedStatement pst = null;
        //           String sql = "INSERT INTO Empleado VALUES(?,?,?,?,?,?)";

        if(Cliente.equals("")|| des.equals("")|| precio.equals("")|| articulo.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            cbViaje.requestFocus();

        }else {

            try{

                pst=cn.prepareStatement("INSERT INTO factura_viaje(num_defactura,fecha_registro,descripcion,valor,articulo,viaje,cliente) VALUES(?,?,?,?,?,?,?)");
                pst.setInt(1,codFactura);//tfidPersona.getText());
            pst.setDate(2,fecha_m);
            pst.setString(3,des);
            pst.setFloat(4,valor);
            pst.setString(5,articulo);
            pst.setInt(6,codViaje);
            pst.setString(7,codCliente);

            //                    JOptionPane.showMessageDialog(null, "Empleado"+ id +"Registrado" );

            int a=pst.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro exitoso");
            }else{
                JOptionPane.showMessageDialog(null,"Error al agregar");
            }
        }catch(HeadlessException | SQLException ex){
            Logger.getLogger(RVehiculo.class.getName()).log(Level.SEVERE, null, ex);
        }

        finally {
            if (cn != null) {
                try {
                    cn.close();
                    pst.close();
                } catch ( SQLException e ) {
                    System.out.println( e.getMessage());
                }
            }
        }

        javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }

        this.cbCliente.setSelectedItem("");
        this.cbViaje.setSelectedItem("");
        this.txtDescripcion.setText("");
        this.txtValor.setText("");
        this.txtArticulo.setText("");

    }//GEN-LAST:event_btnguardarActionPerformed

